function byModelSimpleHtml(estimates: HistorySpendEstimates | undefined): string {
  if (!estimates?.byModel.length) {
    return `<section><h2>By model (API usage)</h2><p class="empty">(none)</p></section>`;
  }
  const rows = estimates.byModel.map((row) => [
    row.model,
    row.inputTokens === undefined ? "—" : String(row.inputTokens),
    row.outputTokens === undefined ? "—" : String(row.outputTokens),
    row.cacheReadTokens === undefined ? "—" : String(row.cacheReadTokens),
    row.cacheWriteTokens === undefined ? "—" : String(row.cacheWriteTokens),
    row.aiCredits === undefined ? "—" : String(row.aiCredits),
    row.estimatedUsdFromPublicRates === undefined
      ? "—"
      : String(row.estimatedUsdFromPublicRates),
  ]);
  const hasPublicUsd = estimates.byModel.some(
    (row) => row.estimatedUsdFromPublicRates !== undefined,
  );
  const note = hasPublicUsd
    ? "API usage from session telemetry. USD column uses public model rates when available."
    : "API usage from session telemetry (always). USD is empty until dollar estimates (public pricing) are requested.";
  return `<section><h2>By model (API usage)</h2>
${table(
  [
    "Model",
    "Input tokens",
    "Output tokens",
    "Cached tokens",
    "Cache write",
    "AI credits",
    "USD (public rates)",
  ],
  rows,
)}
<p class="note">${esc(note)}</p>
</section>`;
}

function byModelAdvancedHtml(estimates: HistorySpendEstimates | undefined): string {
  if (!estimates) {
    return `<section><h2>Spend &amp; models</h2><p class="empty">(none)</p></section>`;
  }
  const summary: string[][] = [
    ["Source", estimates.source],
    ["AI credits", estimates.aiCredits === undefined ? "—" : String(estimates.aiCredits)],
  ];
  if (estimates.pricing) {
    summary.push(
      ["Public pricing attempted", estimates.pricing.attempted ? "yes" : "no"],
      ["Pricing URL", estimates.pricing.sourceUrl ?? "—"],
      ["Retrieved at", formatUkDateTime(estimates.pricing.retrievedAt)],
      ["Matched models", estimates.pricing.matchedModels.join(", ") || "—"],
      ["Unresolved models", estimates.pricing.unresolvedModels.join(", ") || "—"],
    );
  }
  const modelRows = estimates.byModel.map((row) => [
    row.model,
    row.totalNanoAiu === undefined ? "—" : String(row.totalNanoAiu),
    row.aiCredits === undefined ? "—" : String(row.aiCredits),
    row.inputTokens === undefined ? "—" : String(row.inputTokens),
    row.outputTokens === undefined ? "—" : String(row.outputTokens),
    row.estimatedUsdFromPublicRates === undefined
      ? "—"
      : String(row.estimatedUsdFromPublicRates),
  ]);
  return `<section><h2>Spend &amp; models</h2>
${table(["Field", "Value"], summary)}
<p class="disclaimer">${esc(estimates.disclaimer)}</p>
${
  modelRows.length
    ? `<h3>By model</h3>${table(
        ["Model", "nano-AIU", "AI credits", "Input tokens", "Output tokens", "USD (public rates)"],
        modelRows,
      )}`
    : ""
}
</section>`;
}

/** Standalone HTML skill-retro report. Default view is simple. No USD (credits). */
export function formatHistoryHtml(
  report: HistoryAnalysis,
  view: HistoryReportView = "simple",
): string {
  const { filters, coverage, skills, sessionUsage, warnings } = report;
  const skillRows = skills.map((row, index) => [
    String(index + 1),
    row.skill,
    String(row.invocations),
    String(row.sessions),
    formatUkDateTime(row.lastInvokedAt),
  ]);
  const associationRows = sessionUsage.singleSkillAssociations.map((row) => [
    row.skill,
    String(row.sessions),
    String(row.sessionsWithTelemetry),
  ]);

  if (view === "simple") {
    const title = `Skill history — simple (${filters.window}, project=${filters.project})`;
    const body = [
      `<section><h2>Snapshot</h2>`,
      table(
        ["Field", "Value"],
        [
          ["Window", filters.window],
          ["Project", filters.project],
          ["Since", formatUkDateTime(filters.since)],
          ["Skill invocations", String(coverage.invocationsCounted)],
          ["Skills used", String(skills.length)],
          ["Sessions with skills", String(coverage.sessionsWithInvocations)],
          [
            "Sessions matched / discovered",
            `${coverage.sessionsMatched} / ${coverage.sessionsDiscovered}`,
          ],
        ],
      ),
      `</section>`,
      `<section><h2>Top skills</h2>`,
      table(["#", "Skill", "Times used", "Sessions", "Last invoked (UK)"], skillRows),
      `</section>`,
      `<section><h2>API usage</h2>`,
      `<p class="note">Session-level totals only (not per-skill). AI credits always included when telemetry exists.</p>`,
      table(
        ["Metric", "Total"],
        (() => {
          const rows = compactApiUsageRows(sessionUsage.totals, sessionUsage.estimates);
          return rows.length ? rows : [["API usage", "—"]];
        })(),
      ),
      `</section>`,
      byModelSimpleHtml(sessionUsage.estimates),
      `<section><h2>Single-skill associations</h2>`,
      table(["Skill", "Sessions", "With telemetry"], associationRows),
      `</section>`,
    ].join("\n");
    return shell(title, view, body, report.generatedAt);
  }

  const shared = sessionUsage.sharedSkillSessions;
  const warningRows = warnings.map((w) => [w.code, String(w.count), w.message]);
  const title = `Skill history — advanced (${filters.window}, project=${filters.project})`;
  const body = [
    `<section><h2>Window &amp; coverage</h2>`,
    table(
      ["Field", "Value"],
      [
        ["Window", filters.window],
        ["Project", filters.project],
        ["Since", formatUkDateTime(filters.since)],
        ["Generated", formatUkDateTime(report.generatedAt)],
        [
          "Sessions matched / discovered",
          `${coverage.sessionsMatched} / ${coverage.sessionsDiscovered}`,
        ],
        ["Sessions with skill invocations", String(coverage.sessionsWithInvocations)],
        ["Invocations counted", String(coverage.invocationsCounted)],
        ["Skill sessions with telemetry", String(coverage.shutdownTelemetrySessions)],
      ],
    ),
    `</section>`,
    `<section><h2>Top skills</h2>`,
    table(["#", "Skill", "Times used", "Sessions", "Last invoked (UK)"], skillRows),
    `</section>`,
    `<section><h2>Session-level usage</h2>`,
    `<p class="note">Attribution: <strong>session-level-only</strong>.</p>`,
    `<p class="note">Skill sessions: <strong>${esc(sessionUsage.sessions)}</strong> (${esc(sessionUsage.sessionsWithTelemetry)} with telemetry)</p>`,
    table(["Metric", "Total", "Metric sessions"], usageRows(sessionUsage)),
    `</section>`,
    byModelAdvancedHtml(sessionUsage.estimates),
    `<section><h2>Single-skill associations</h2>`,
    table(["Skill", "Sessions", "With telemetry"], associationRows),
    `</section>`,
    `<section><h2>Shared skill sessions</h2>`,
    `<p class="note">Shared sessions: <strong>${esc(shared.sessions)}</strong> (${esc(shared.sessionsWithTelemetry)} with telemetry)</p>`,
    table(["Metric", "Total", "Metric sessions"], usageRows(shared)),
    `</section>`,
    `<section><h2>Warnings</h2>`,
    table(["Code", "Count", "Message"], warningRows),
    `</section>`,
  ].join("\n");
  return shell(title, view, body, report.generatedAt);
}
