import type { HistoryAnalysis, UsageBucket } from "./analyze.js";
import type { HistorySpendEstimates } from "./cost-estimate.js";
import { formatUkDateTime, type HistoryReportView } from "./report-view.js";

const METRIC_LABELS: Record<string, string> = {
  inputTokens: "Input tokens",
  cachedInputTokens: "Cached input tokens",
  cacheWriteTokens: "Cache write tokens",
  outputTokens: "Output tokens",
  totalTokens: "Total tokens",
  totalNanoAiu: "AI credits (nano-AIU)",
  premiumRequests: "Premium requests",
  durationMs: "API duration (ms)",
};

function esc(value: string | number | undefined | null): string {
  if (value === undefined || value === null || value === "") return "—";
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

function table(headers: string[], rows: string[][]): string {
  if (rows.length === 0) {
    return `<p class="empty">(none)</p>`;
  }
  const thead = `<tr>${headers.map((h) => `<th>${esc(h)}</th>`).join("")}</tr>`;
  const tbody = rows
    .map((row) => `<tr>${row.map((c) => `<td>${esc(c)}</td>`).join("")}</tr>`)
    .join("\n");
  return `<div class="table-wrap"><table>\n<thead>${thead}</thead>\n<tbody>\n${tbody}\n</tbody>\n</table></div>`;
}

function usageRows(bucket: UsageBucket): string[][] {
  return Object.entries(bucket.totals)
    .filter(([, value]) => value !== undefined)
    .map(([key, value]) => {
      const sessions = bucket.metricSessions[key as keyof typeof bucket.metricSessions] ?? 0;
      return [METRIC_LABELS[key] ?? key, String(value), String(sessions)];
    });
}

function compactApiUsageRows(
  totals: UsageBucket["totals"],
  estimates: import("./cost-estimate.js").HistorySpendEstimates | undefined,
): string[][] {
  const rows: string[][] = [];
  const nano = totals.totalNanoAiu;
  const aiCredits =
    estimates?.aiCredits ??
    (typeof nano === "number" ? Number((nano / 1_000_000_000).toFixed(9)) : undefined);
  if (aiCredits !== undefined) rows.push(["AI credits", String(aiCredits)]);
  if (nano !== undefined) rows.push(["AI credits (nano-AIU)", String(nano)]);
  for (const key of [
    "totalTokens",
    "inputTokens",
    "outputTokens",
    "cachedInputTokens",
    "cacheWriteTokens",
    "premiumRequests",
    "durationMs",
  ] as const) {
    if (totals[key] !== undefined) {
      rows.push([METRIC_LABELS[key] ?? key, String(totals[key])]);
    }
  }
  return rows;
}

